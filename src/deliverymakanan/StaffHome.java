    private void btSave2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSave2ActionPerformed
        // TODO add your handling code here:
        ArrayList<Order> order=new ArrayList<>();

        
        int i=0;
        while(tbOrder.getValueAt(i, 0)!=null){
            String[] s=new String[8];
            for(int j=0; j<s.length; j++){
                s[j]=new String();
            }
            s[0]=toString(tbOrder.getValueAt(i, 0));
            s[1]=toString(tbOrder.getValueAt(i, 1));
            s[2]=toString(tbOrder.getValueAt(i, 2));
            s[3]=toString(tbOrder.getValueAt(i, 3));
            s[4]=toString(tbOrder.getValueAt(i, 4));
            s[5]=toString(tbOrder.getValueAt(i, 5));
            s[6]=toString(tbOrder.getValueAt(i, 6));
            
            Customer ctemp = null;
            Branch btemp = null;
            Staff stemp = null;
            Order odtemp = null;
            
            for(int j=0; j<sf.dr.order.size(); j++){
                if(s[0].equals(sf.dr.order.get(j).idorder)){
                    odtemp=sf.dr.order.get(j);
                }
            }
            for(int j=0; j<sf.dr.customer.size(); j++){
                if(s[2].equals(sf.dr.customer.get(j).idcust)){
                    ctemp=sf.dr.customer.get(j);
                }
            }
            
            for(int j=0; j<sf.dr.branch.size(); j++){
                if(s[4].equals(sf.dr.branch.get(j).idbranch)){
                    btemp=sf.dr.branch.get(j);
                }
            }
            for(int j=0; j<sf.dr.staff.size(); j++){
                if(s[3].equals(sf.dr.staff.get(j).idstaff)){
                    stemp=sf.dr.staff.get(j);
                }
            }
            
            Order od=new Order(s[1], odtemp.idorder, odtemp.item, ctemp, stemp, btemp, odtemp.address);
            od.stateDelivered=Boolean.valueOf(s[5]);
            od.statePayment=Boolean.valueOf(s[6]);
            order.add(od);
            
            i++;
        }
        sf.dr.order=order;
        try {
            sf.dr.saveData(sf.dr);
        } catch (IOException ex) {
            Logger.getLogger(StaffHome.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(null, "Save data Order berhasil !!");
    }//GEN-LAST:event_btSave2ActionPerformed

