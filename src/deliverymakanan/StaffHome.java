    private void btSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSaveActionPerformed
        // TODO add your handling code here:
        ArrayList<Customer> cust=new ArrayList<>();

        int i=0;
        while(tbPelanggan.getValueAt(i, 0)!=null){
            
            Customer c=new Customer();
            String[] s=new String[8];
            for(int j=0; j<s.length; j++){
                s[j]=new String();
            }
            s[0]=toString(tbPelanggan.getValueAt(i, 2));
            s[1]=toString(tbPelanggan.getValueAt(i, 6));
            s[2]=toString(tbPelanggan.getValueAt(i, 1));
            s[3]=toString(tbPelanggan.getValueAt(i, 7));
            s[4]=toString(tbPelanggan.getValueAt(i, 4));
            s[5]=toString(tbPelanggan.getValueAt(i, 5));
            s[6]=toString(tbPelanggan.getValueAt(i, 0));
            
            c.setCustomer(s[0], s[1], s[2], s[3], s[4], s[5], s[6]);
            c.state_member=Boolean.valueOf(toString(tbPelanggan.getValueAt(i, 3)));
            cust.add(c);
            
            i++;
        }
        sf.dr.customer=cust;
        try {
            sf.dr.saveData(sf.dr);
        } catch (IOException ex) {
            Logger.getLogger(StaffHome.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(null, "Save data pelanggan berhasil !!");
    }//GEN-LAST:event_btSaveActionPerformed

